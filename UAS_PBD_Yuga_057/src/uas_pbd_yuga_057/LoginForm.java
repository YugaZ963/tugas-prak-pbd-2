    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try {
            // Mendefinisikan query SQL untuk memilih akun berdasarkan username dan password
            String sql = "SELECT * FROM akun WHERE username=? AND password=?";
    
            // Membuat koneksi ke database menggunakan konfigurasi dari Config.configDB()
            java.sql.Connection con = (Connection) Config.configDB();

            // Membuat PreparedStatement untuk eksekusi query SQL yang telah didefinisikan
            java.sql.PreparedStatement pst = con.prepareStatement(sql);

            // Mengatur nilai parameter pertama (?) pada query dengan teks dari txtUsername
            pst.setString(1, txtUsername.getText());

            // Mengatur nilai parameter kedua (?) pada query dengan teks dari txtPass
            pst.setString(2, txtPass.getText());

            // Mengeksekusi query dan menyimpan hasilnya dalam ResultSet
            java.sql.ResultSet rs = pst.executeQuery();

            // Memeriksa apakah ResultSet memiliki baris (artinya ada akun yang cocok)
            if (rs.next()) {
                // Mendapatkan nilai ID dari akun yang cocok
                int userId = rs.getInt("id");

                // Mendapatkan username dari akun yang cocok
                String username = rs.getString("username");

                // Menyimpan userId ke dalam kelas UserSession
                UserSession.userId = userId;
        
                // Menyimpan username ke dalam kelas UserSession
                UserSession.username = username;

                // Mendapatkan peran (role) dari akun yang cocok
                String role = rs.getString("role");

                // Memeriksa apakah input username dan password cocok dengan yang ada di database
                if (txtUsername.getText().equals(rs.getString("username")) && txtPass.getText().equals(rs.getString("password"))) {
                    // Menampilkan pesan bahwa login berhasil
                    JOptionPane.showMessageDialog(null, "berhasil login");

                    // Memeriksa apakah peran (role) adalah admin
                    if (role.equals("admin")) {
                        // Menyembunyikan frame login
                        this.setVisible(false);

                        // Menampilkan frame admin
                        new AdminFrame().setVisible(true);
                    } else if (role.equals("user")) {
                        // Menyembunyikan frame login
                        this.setVisible(false);

                        // Menampilkan frame user
                        new MainFrame057().setVisible(true);
                    }
                } else {
                    // Menampilkan pesan bahwa username atau password salah
                    JOptionPane.showMessageDialog(null, "username atau password salah !!");
                }
            }
    
        } catch (Exception e) {
            // Menampilkan pesan kesalahan jika terjadi exception
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_btnLoginActionPerformed

