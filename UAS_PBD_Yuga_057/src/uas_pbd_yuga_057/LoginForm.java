    private void btnShowPassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowPassActionPerformed
        // Mengecek apakah password saat ini terlihat (isPasswordVisible adalah true)
        if (isPasswordVisible) {
            // Mengatur ulang karakter echo untuk menyembunyikan teks password
            // '*' adalah karakter bullet default yang digunakan untuk menyembunyikan password
            // Anda bisa mengganti '*' dengan karakter lain yang diinginkan untuk menyembunyikan password
            txtPass.setEchoChar('*');

            // Mengubah teks tombol menjadi "Show Password" untuk memberikan indikasi bahwa klik berikutnya akan menampilkan password
            btnShowPass.setText("Show Password");
        } else {
            // Menampilkan teks password sebagai teks biasa dengan mengatur karakter echo menjadi null (0)
            // Mengatur karakter echo ke (char) 0 akan menampilkan teks asli tanpa perubahan
            txtPass.setEchoChar((char) 0);

            // Mengubah teks tombol menjadi "Hide Password" untuk memberikan indikasi bahwa klik berikutnya akan menyembunyikan password
            btnShowPass.setText("Hide Password");
        }

        // Membalik nilai dari isPasswordVisible;
        // jika sebelumnya true maka menjadi false, dan sebaliknya
        // Ini akan digunakan pada klik tombol berikutnya untuk menentukan apakah password harus ditampilkan atau disembunyikan
        isPasswordVisible = !isPasswordVisible;
    }//GEN-LAST:event_btnShowPassActionPerformed

